package arsenal.metiz.catalog;

public class Requests {

    public String mark;
    public String diameter;
    public String pack;
    public String layer;
    public String amount;
    public String cname;
    public String email;
    public String status;

    public Requests(String mark, String diameter,String pack,String layer,String amount,String cname, String email, String status) {
        this.mark = mark;
        this.diameter = diameter;
        this.pack = pack;
        this.layer = layer;
        this.amount = amount;
        this.cname = cname;
        this.email = email;
        this.status = status;
    }
}
